package br.edu.unifei.ecot12.t3.projetoFinal.leagueOfLegends;

public class Torre {

	private int dano;
	private int alcance;
	private int visao;
	private int defesa;
	private int vida;
	private int ouroDado;
	private int xpDado;

	public int getDano() {
		return dano;
	}

	public void setDano(int dano) {
		this.dano = dano;
	}

	public int getAlcance() {
		return alcance;
	}

	public void setAlcance(int alcance) {
		this.alcance = alcance;
	}

	public int getVisao() {
		return visao;
	}

	public void setVisao(int visao) {
		this.visao = visao;
	}

	public int getDefesa() {
		return defesa;
	}

	public void setDefesa(int defesa) {
		this.defesa = defesa;
	}

	public int getVida() {
		return vida;
	}

	public void setVida(int vida) {
		this.vida = vida;
	}

	public int getOuroDado() {
		return ouroDado;
	}

	public void setOuroDado(int ouroDado) {
		this.ouroDado = ouroDado;
	}

	public int getXpDado() {
		return xpDado;
	}

	public void setXpDado(int xpDado) {
		this.xpDado = xpDado;
	}

}
