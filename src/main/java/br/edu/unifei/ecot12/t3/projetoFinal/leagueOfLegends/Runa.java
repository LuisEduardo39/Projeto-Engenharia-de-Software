package br.edu.unifei.ecot12.t3.projetoFinal.leagueOfLegends;

public class Runa {

	private String caminhoPrimario;
	private String Descricao;
	private String caminhoSecundario;

	public String getCaminhoPrimario() {
		return caminhoPrimario;
	}

	public void setCaminhoPrimario(String caminhoPrimario) {
		this.caminhoPrimario = caminhoPrimario;
	}

	public String getDescricao() {
		return Descricao;
	}

	public void setDescricao(String descricao) {
		Descricao = descricao;
	}

	public String getCaminhoSecundario() {
		return caminhoSecundario;
	}

	public void setCaminhoSecundario(String caminhoSecundario) {
		this.caminhoSecundario = caminhoSecundario;
	}

}
